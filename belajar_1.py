# Ini adalah komentar

# Mendefinisikan variabel
nama = "Febiola Aruan"
umur = 18

# Mencetak nilai variabel
print("Nama saya adalah", nama)
print("Umur saya", umur, "tahun")

# Percabangan (if-else)
if umur >= 18:
    print("Anda sudah dewasa")
else:
    print("Anda masih di bawah umur")

# Perulangan (for)
for i in range(4):
    print(i)

